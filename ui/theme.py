"""QSS stylesheet generator — turns a `backend.constants.THEMES[name]` color
dict into a full application stylesheet string.

There is no Tkinter equivalent to port here 1:1: the original configured
~15 individual ttk.Style() rules scattered across `apply_theme`/
`refresh_themed_widgets`. QSS covers the same ground in one place, the
idiomatic Qt way, so this module is a clean reimplementation against the
same color tokens rather than a line-by-line port. Every widget class the
app actually uses gets its own block below; nothing here references a
backend value other than the color dict.

`.accent` / `.danger` / `.ghost` are QPushButton object-name variants
(`button.setObjectName("accent")`, etc.) — the original's
"Accent.TButton" / "Danger.TButton" / "Ghost.TButton" ttk styles.
"""
