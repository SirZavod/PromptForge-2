def apply_theme(app: QApplication, theme) -> None:
    """Generates and applies the stylesheet for `theme` (a `THEMES` key
    or a colors dict — see `build_qss`) to the whole application in one
    call — `app.setStyleSheet(...)` cascades to every widget, current
    and future, the same way the original's `apply_theme`/
    `refresh_themed_widgets` pair updated every ttk.Style rule at once
    on a dark/light toggle."""
    app.setStyleSheet(build_qss(theme))
